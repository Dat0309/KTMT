.MODEL SMALL
.STACK 100h
.DATA
MSG1 DB 10,13, 'NHAP MOT KY TU: $'
MSG2 DB 10,13, '5 KY TU DUNG TRUOC LA: $'
MSG3 DB 10,13, '5 KY TU DUNG SAU LA: $'

KYTU DB ?

.CODE
MOV AX,@DATA
MOV DS,AX

MOV AH,9
LEA DX,MSG1
INT 21H

;NHAP
MOV AH,1
INT 21H
MOV KYTU,AL

;5 KY TU DUNG TRUOC
MOV AH,9
LEA DX,MSG2
INT 21H

MOV DL,KYTU
SUB DL,5
MOV CX,5

MOV AH,2
PRINT_PRE:
INT 21H
INC DL
LOOP PRINT_PRE

;XUAT 5 KY TU SAU
MOV AH,9
LEA DX,MSG3
INT 21H

MOV DL,KYTU
INC DL
MOV CX,5

MOV AH,2
PRINT_POS:
INT 21H
INC DL
LOOP PRINT_POS

MOV AH,4Ch
INT 21H
END